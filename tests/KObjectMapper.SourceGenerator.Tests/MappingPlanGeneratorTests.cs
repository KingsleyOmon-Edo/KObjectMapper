        await test.RunAsync();
    }

    // ── US-07 AC3: Build diagnostics identify unsupported member patterns ──

    [Fact]
    public async Task Generator_EmitsKOM001Warning_WhenSourcePropertyHasNoMatchOnTarget()
    {
        string source = """
            using KObjectMapper.Configuration;

            namespace MyApp;

            public class Source { public string Name { get; set; } = ""; public string ExtraField { get; set; } = ""; }
            public class Target { public string Name { get; set; } = ""; }

            public class MyProfile : MappingProfile
            {
                protected override void Configure()
                {
                    CreateMap<Source, Target>();
                }
            }
            """;

        CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> test = BuildTest(source);

        // Expect KOM001 warning for ExtraField not found on Target
        test.TestState.ExpectedDiagnostics.Add(
            new DiagnosticResult("KOM001", DiagnosticSeverity.Warning)
                .WithMessage("Source property 'Source.ExtraField' has no matching writable property on 'Target'."));

        test.TestState.GeneratedSources.Add(
            ($"{typeof(MappingPlanGenerator).Assembly.GetName().Name}\\KObjectMapper.SourceGenerator.MappingPlanGenerator\\SourceToTargetMapper.g.cs",
             SourceText.From(GetExpectedNameOnlyMapper(), Encoding.UTF8)));

        await test.RunAsync();
    }

    [Fact]
    public async Task Generator_EmitsKOM001Warning_WhenTypeMismatchPreventsDirectAssignment()
    {
        string source = """
            using KObjectMapper.Configuration;

            namespace MyApp;

            public class Source { public string Count { get; set; } = ""; }
            public class Target { public int Count { get; set; } }

            public class MyProfile : MappingProfile
            {
                protected override void Configure()
                {
                    CreateMap<Source, Target>();
                }
            }
            """;

        CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> test = BuildTest(source);

        // Expect KOM001 warning for type mismatch (string vs int)
        test.TestState.ExpectedDiagnostics.Add(
            new DiagnosticResult("KOM001", DiagnosticSeverity.Warning)
                .WithMessage("Type mismatch: 'Source.Count' (string) cannot be directly assigned to 'Target.Count' (int). A type converter may be required."));

        // No generated source because no mappable properties remain
        await test.RunAsync();
    }

    [Fact]
    public async Task Generator_ProducesMultipleMappers_ForMultipleCreateMapCalls()
    {
        string source = """
            using KObjectMapper.Configuration;

            namespace MyApp;

            public class A { public string X { get; set; } = ""; }
            public class B { public string X { get; set; } = ""; }
            public class C { public string X { get; set; } = ""; }
            public class D { public string X { get; set; } = ""; }

            public class MultiProfile : MappingProfile
            {
                protected override void Configure()
                {
                    CreateMap<A, B>();
                    CreateMap<C, D>();
                }
            }
            """;

        CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> test = BuildTest(source);
        test.TestState.GeneratedSources.Add(
            ($"{typeof(MappingPlanGenerator).Assembly.GetName().Name}\\KObjectMapper.SourceGenerator.MappingPlanGenerator\\AToBMapper.g.cs",
             SourceText.From(GetExpectedXMapper("MyApp.A", "MyApp.B", "AToBMapper"), Encoding.UTF8)));
        test.TestState.GeneratedSources.Add(
            ($"{typeof(MappingPlanGenerator).Assembly.GetName().Name}\\KObjectMapper.SourceGenerator.MappingPlanGenerator\\CToDMapper.g.cs",
             SourceText.From(GetExpectedXMapper("MyApp.C", "MyApp.D", "CToDMapper"), Encoding.UTF8)));

        await test.RunAsync();
    }

    // ── Helpers ──────────────────────────────────────────────────────────────────

    private static CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> BuildTest(string source)
    {
        CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> test = new()
        {
            TestState =
            {
                Sources =
                {
                    // Provide a stub MappingProfile so the semantic model resolves the base class
                    ("MappingProfileStub.cs", MappingProfileStub),
                    ("TestSource.cs", source)
                }
            }
        };
        return test;
    }

    private const string MappingProfileStub = """
        namespace KObjectMapper.Configuration;

        public abstract class MappingProfile
        {
            protected MappingProfile() { Configure(); }
            protected abstract void Configure();
            protected object CreateMap<TSource, TTarget>() => null!;
        }
        """;


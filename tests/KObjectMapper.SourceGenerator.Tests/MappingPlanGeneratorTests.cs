using System.Text;
using System.Threading.Tasks;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Testing;
using Microsoft.CodeAnalysis.Testing;
using Microsoft.CodeAnalysis.Text;
using Xunit;

namespace KObjectMapper.SourceGenerator.Tests;

/// <summary>
/// TDD tests for MappingPlanGenerator (US-07).
/// Verifies that the incremental source generator produces correct compile-time
/// mapping implementations for registered source/target pairs.
/// </summary>
public class MappingPlanGeneratorTests
{
    // ── US-07 AC1: Incremental source generator produces mapping implementations ──

    [Fact]
    public async Task Generator_ProducesMapperClass_ForSimpleTypePair()
    {
        string source = """
            using KObjectMapper.Configuration;

            namespace MyApp;

            public class Source { public string Name { get; set; } = ""; public int Age { get; set; } }
            public class Target { public string Name { get; set; } = ""; public int Age { get; set; } }

            public class MyProfile : MappingProfile
            {
                protected override void Configure()
                {
                    CreateMap<Source, Target>();
                }
            }
            """;

        string expectedGenerated = """
            // <auto-generated/>
            // Generated by KObjectMapper.SourceGenerator — do not edit manually.

            namespace KObjectMapper.Generated;

            /// <summary>
            /// Compile-time generated mapper from <see cref="MyApp.Source"/> to <see cref="MyApp.Target"/>.
            /// </summary>
            public static class SourceToTargetMapper
            {
                /// <summary>Maps all compatible properties from <paramref name="source"/> into <paramref name="target"/>.</summary>
                public static void Map(MyApp.Source source, MyApp.Target target)
                {
                    if (source is null) throw new System.ArgumentNullException(nameof(source));
                    if (target is null) throw new System.ArgumentNullException(nameof(target));

                    target.Name = source.Name;
                    target.Age = source.Age;
                }

                /// <summary>Creates a new <see cref="MyApp.Target"/> and maps all compatible properties from <paramref name="source"/>.</summary>
                public static MyApp.Target Map(MyApp.Source source)
                {
                    MyApp.Target target = new();
                    Map(source, target);
                    return target;
                }
            }

            """;

        CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> test = BuildTest(source);
        test.TestState.GeneratedSources.Add(
            ($"{typeof(MappingPlanGenerator).Assembly.GetName().Name}\\KObjectMapper.SourceGenerator.MappingPlanGenerator\\SourceToTargetMapper.g.cs",
             SourceText.From(expectedGenerated, Encoding.UTF8)));

        await test.RunAsync();
    }

    [Fact]
    public async Task Generator_ProducesNoOutput_WhenNoMappingProfileSubclassPresent()
    {
        string source = """
            namespace MyApp;
            public class Source { public string Name { get; set; } = ""; }
            public class Target { public string Name { get; set; } = ""; }
            public class NotAProfile { }
            """;

        CSharpSourceGeneratorTest<MappingPlanGenerator, DefaultVerifier> test = BuildTest(source);
        // No generated sources expected
        await test.RunAsync();
    }

    // ── US-07 AC2: Generated mappers compile without consumer-written partial methods ──

